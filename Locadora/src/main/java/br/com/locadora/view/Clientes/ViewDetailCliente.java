    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       //String nome, String login, String senha, String email, int id, String documento, String telefone, Locadora locadora) 
        try {
            Cliente cli = new Cliente(
                    jtfNome.getText(),
                    jtfLogin.getText(),
                    jftSenha.getText(),
                    null,
                    0,
                    jftDoc.getText(),
                    jtfTelefone.getText(),
                    new Locadora(1));

            if (condicionalCampo(cli)){
                return;
            }
            
            if (cliente == null) {
                new ClienteDAO().inserir(cli);
            }else {
                cli.setId(cliente.getId());
                new ClienteDAO().alterar(cli);
            }

            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");

            setVisible(false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "dados incorretos");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

